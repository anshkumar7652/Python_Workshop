#string accesing multiple characters of a string using slicing method

#formula: [(start_index_no.) : (end_index_no. +1) : gap(increasing or decreasing)]

a='Enter your name'
#gap uses positive index
print(a[0:5:2])
print(a[0:5:1])
print(a[11:15:1])
print(a[6:10:1])

#gap uses positive index
print(a[-1:-5:-1])
